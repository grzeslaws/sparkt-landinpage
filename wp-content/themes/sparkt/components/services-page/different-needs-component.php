        <h1 class="c-different-needs__headline u-detect-to-hide">Different needs, different approaches</h1>
        <p class="c-different-needs__description u-detect-to-hide">We offer three different levels of telematics solutions, each of them suited best for different use-case and specific customer needs:</p>
        <div class="c-different-needs__box c-different-needs__box--1 u-detect-to-hide">
            <h3 class="c-different-needs__headline-box">
                <span class="c-different-needs__headline-id">01</span>    
                Entry level
            </h3>
            <p class="c-different-needs__description-box">Data is collected from sensors in the driver’s mobile phone (such as GPS and accelerometer). The solution is easy to introduce, even on a mass market (as no additional hardware is required), but it offers only limited precision of the analysis. No car diagnostic is available. The precision of acceleration detection is lower than in case of high precision telematics.</p>
        </div>
        <div class="c-different-needs__box c-different-needs__box--2 u-detect-to-hide">
            <h3 class="c-different-needs__headline-box">
                <span class="c-different-needs__headline-id">02</span>    
                Entry level
            </h3>
            <p class="c-different-needs__description-box">Data is collected both from sensors in a phone and from additional devices that read diagnostic information from the vehicle. This approach allows us to include much more parameters in the analysis, resulting in more accurate drivers’ profiles, but it requires additional hardware to be installed in each vehicle.</p>
        </div>
        <div class="c-different-needs__box c-different-needs__box--3 u-detect-to-hide">
            <h3 class="c-different-needs__headline-box">
                <span class="c-different-needs__headline-id">03</span>    
                Entry level
            </h3>
            <p class="c-different-needs__description-box">In our next-generation telematics solutions employ sophisticated state-of-the-art machine learning algorithms and additional sensors (such as a camera or radar) to achieve superior analysis precision.</p>
